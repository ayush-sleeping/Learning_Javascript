/*  Inheritance and Prototype chain :

Inheritance is an object oriented programming concept
(means that one object is based on another object.). 

So how does inheritance works in JavaScript?
Each and every JavaScript object has a property called prototype.
This prototype property makes it possible to inherit methods and properties to the child object.

Every object in JavaScript has a built-in property, which is called its prototype.
The prototype is itself an object, so the prototype will have its own prototype,
making what's called a prototype chain. The chain ends when we reach a prototype that has null for its own prototype.

*/