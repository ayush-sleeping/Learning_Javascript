/* BOM :
Browser Object Model represents the current browser window.
The main purpose of Browser Object Model is to manipulate the 
properties and methods which are associated with the browser window.

The Object present in Window are 
Document, History, Location, Screen Navigator .

Yo access the BOM you can use The "window" Object .
For example :

window.document.getElementById('topbar')
document.getElementById('topbar')

window.alert('This is window')
alert('This is without window')



This window object also gives use access to some methods like :
- setTimeout()
- setInterval()
- alert()
- confirm()
- prompt()
- open()
- close()

*/