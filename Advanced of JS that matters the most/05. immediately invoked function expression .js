// Immediately Invoked Function Expression (IIFE) ::
/*
 * An IIFE is a function that is executed as soon as it is defined.
 * It is a common pattern in JavaScript to create a new scope and avoid polluting the global namespace.
 * Syntax:
 * (function() {
 *   // code here
 * })();
 * IIFEs are often used for:
 * - Module pattern
 * - Encapsulating variables
 * - Creating private scopes
 * So basically, IIFEs help in avoiding global variable pollution and can create private variables.
*/

