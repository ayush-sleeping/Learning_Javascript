// New Keyword ::
/*
 * The `new` keyword is used to create an instance of an object.
 * It does the following:
 * 1. Creates a new object.
 * 2. Sets the prototype of the new object to the constructor's prototype.
 * 3. Binds `this` to the new object.
 * 4. Returns the new object (unless the constructor returns a non-primitive value).
 * This allows for the creation of multiple instances of an object with shared methods and properties.
*/

