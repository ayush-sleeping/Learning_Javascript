/* Query Selectors :
Query Selectors allow you to use CSS selectors to select HTML
elements. It is the new way in JavaScript to select HTML element.

There are 2 such selectors :
querySelector()
querySelectorAll()
*/

document.querySelectorAll('.blog-card img')
document.querySelector('.blog-card img') 

document.querySelectorAll('#blog-card img')
document.querySelector('#blog-card img') 