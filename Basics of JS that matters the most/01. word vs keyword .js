// Word vs KeyWord :

/* In JavaScript, a "word" is any sequence of characters that can be used as an identifier,
such as variable names, function names, etc. */

/* A "keyword" is a reserved word in JavaScript that has a special meaning and cannot be used as an identifier. Keywords are part of the JavaScript language syntax and are used to define */

// In example :
let myVariable = 10; // "myVariable" is a word (identifier), whereas "let" is a keyword in JavaScript".
