// Arithmetic Operators :
// Arithmetic Operators available in "JavaScript" are 
// "+" , "-" , "*" , "/" , "%" - Modulus , "++" - Increment , "--" - Decrement.

console.clear()

var num1 = 10;
var num2 = 15;
var num3 = 20;

// Addition
console.log(num1 + num2)
console.log(num1 + num2 + num3)

//Subtraction
console.log(num1 - num2)
console.log(num3 - num2)

//Multiplication
console.log(num1 * num2)
console.log(num1 * num2 * num3)

//Division
console.log(num1 / num2)
console.log(num3 / num1)

//Modulus
console.log(num1 % num2)
console.log(num3 % num2)

//Incrment - Post and Pre
//Post
console.log(num1++)
console.log(num1++)
console.log(num1)

//Pre
console.log(++num2)
console.log(num2)

//Decrement
//Post :
console.log(num3--)
console.log(num3)

//Pre
console.log(--num2)
console.log(num2)