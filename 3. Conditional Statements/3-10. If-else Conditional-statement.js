// How to use IF-else statement in JS

console.clear()

if(7 > 5){
    console.log(true)
}

// second example: here also using "else"
if (5 > 7){
    console.log(true)
} else {
    console.log(false)
}

// Checking multiple statement : if-else if
if (6 > 8){
    console.log('6 > 8')
}else if(7 > 8){
    console.log('7 > 8')
}else{
    console.log('Nothing')
}

